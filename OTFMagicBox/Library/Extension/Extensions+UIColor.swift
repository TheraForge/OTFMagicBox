import SwiftUI

/**
  Extension of UIColor to use custom color for an application.
 */
public extension UIColor {
    
    static var primaryColor = Color(red: 252/255, green: 100/255, blue: 75/255,opacity: 0.8)
    
    static var secondaryColor = Color(red: 34/255, green: 42/255, blue: 56/255)
    
}
